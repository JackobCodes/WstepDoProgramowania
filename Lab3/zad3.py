# Mając podana listę ulic w danej dzielnicy i wiedząc, że na każdej ulicy znajduje się 5 bloków
# mieszkalnych, a w każdym z nich jest 10 lokali, wypisz listę wszystkich adresów w dzielnicy.
# Lista ulic w dzielnicy:
# • Jagodowa,
# • Lipowa,
# • Kwiatowa,
# • Kasztanowa,
# • Polna.

ulice = ["Jagodowa", "Lipowa", "Kwiatowa", "Kasztanowa", "Polna"]

adresy = []

for ulica in ulice:
    for blok in range(1, 6):
        for lokal in range(1, 11):
            adresy.append(f"{ulica} {blok}/{lokal}")

for adres in adresy:
    print(adres)

