# Sprawdź w interpreterze typ wyników określonych działań - type( x ) i wyjaśnij, co
# oznaczają poszczególne operatory?

a = 1 + 2
# <class 'int'> - operator + oznacza dodawanie

b = 1 + 4.5
# <class 'float'> - operator + oznacza dodawanie

c = 3 / 2
# <class 'float'> - operator / oznacza dzielenie zmiennoprzecinkowe

d = 4 / 2
# <class 'float'> - operator / oznacza dzielenie zmiennoprzecinkowe

e = 3 // 2
# <class 'int'> - operator // oznacza dzielenie całkowite

f = -3 // 2
# <class 'int'> - operator // oznacza dzielenie całkowite

g = 11 % 2
# <class 'int'> - operator % oznacza dzielenie modulo (reszta z dzielenia)

h = 2 ** 10
# <class 'int'> - operator ** oznacza potęgowanie

i = 8 ** (1/3)
# <class 'float'> - operator ** oznacza potęgowanie

